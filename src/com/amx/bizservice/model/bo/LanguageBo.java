package com.amx.bizservice.model.bo;


public class LanguageBo {
	
	/**
     * @主键.
     */
    private int id; 


    /**
     * @语言名称（中文）.
     */
    private String name;


	public int getId() {
		return id;
	}


	public void setId(int id) {
		this.id = id;
	}


	public String getName() {
		return name;
	}


	public void setName(String name) {
		this.name = name;
	} 

}
